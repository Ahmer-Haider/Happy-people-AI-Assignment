/* Problem 4: Define variables for different Data Types int, Boolean, char, float,
   double and print on the Console
*/

package Assignment.Basics;

public class DataTypes {
    public static void main(String[] args) {
        String str = "This is String Data type";
        int num = 2025;
        float decimalNum = 20.25f;
        double decNum = 20.25254;
        boolean bool = true;
        char ch = 'a';

        System.out.println("String Data Type: "+str);
        System.out.println("Integer Data Type: "+num);
        System.out.println("Float Data Type: "+decimalNum);
        System.out.println("Double Data Type: "+decNum);
        System.out.println("Boolean Data Type: "+bool);
        System.out.println("Char Data Type: "+ch);

    }
}
